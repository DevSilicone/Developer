package day21_loops_review;

public class LoopsPractice4 {

	public static void main(String[] args) {
		
		String word2   = "bananas";
		String unique2 = "";
		
		int i = 0;
		char ch = word2.charAt(i);	//b
		System.out.println("Ch1: " + ch);
		
		
		
		i = 1;
		ch = word2.charAt(i);	//a
		System.out.println("Ch2: " + ch);
		
		
		i = 2;
		ch = word2.charAt(i);	//n
		System.out.println("Ch3: " + ch);
		
		
		i++;
		ch = word2.charAt(i);	//a
		System.out.println("Ch4: " + ch);
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
