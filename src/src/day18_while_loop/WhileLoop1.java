package day18_while_loop;

public class WhileLoop1 {

	public static void main(String[] args) {
		
		int n1 = 100;
		
		int n2 = 140;
		
			while (n2 > n1 ) {
				System.out.println("N2 is greater");
			}
			
			System.out.println("END"); 
			// THE ABOVE LOOP IS INFINITE LOOP. 
			// THE CONCITION n2>n1 WILL ALWAYS BE TRUE, AND EVER BECOMES FALSE
			// WHILE LOOP KEEPS EXECUTION AS LONG AS THE CONDITION IS/STAYS TRUE.
				
				
				
	}

}
