package day06_operators2_Scanner;

public class EvenOrOdd {

	public static void main(String[] args) {
		
		int num = 5;
		System.out.println(num % 2); // odd
		
		num = 10;
		
		System.out.println(num % 2); // even
		
		num = 55;
		
		System.out.println(num % 2); // odd
		
		
		

	}

}
