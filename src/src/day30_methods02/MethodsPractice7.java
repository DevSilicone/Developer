package day30_methods02;

import java.util.*;

public class MethodsPractice7 {

	public static void main(String[] args) {
	
		sleep(7);
		sleep(3);
		sleep(5);
		sleep(11);	
	}
	
	/**
	 Method name: sleep
	 return type: void
	 params/args/input: int hours
	 
	 Method accept hours value and prints
	 "Student has been sleeping for 5 hours"
	 */
	
	public static void sleep(int hours) {
		System.out.println("Student has been sleeping for " +hours+" hours.");
	}
	
	
	
	
	
}
